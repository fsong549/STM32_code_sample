#include "stm32f10x.h"
#include "led.h"

int main(void)
{

	uint32_t		i;								//定义32位数

	LED_Init(); 									//初始化LED

	LED2_ON;
	LED3_OFF;

	for (i = 0; i < 0xffffff; i++)
		; //for循环不精确延时	

	while (1)
	{
		for (i = 0; i < 0x6ffff; i++)
			; //for循环不精确延时

		LED2_REV;									//LED2取反
		LED3_REV;									//LED3取反
	}
}


